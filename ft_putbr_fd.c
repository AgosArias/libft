#include "libft.h"

void	ft_putnbr_fd(int nb)
{
	if (nb >= 10)
	{
		ft_putnbr_fd (nb / 10);
		ft_putchar_fd (nb % 10 + '0');
	}
	else
	{
		ft_putchar_fd(nb + '0');
	}
}
